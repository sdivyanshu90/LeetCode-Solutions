from typing import List

class Solution:
    def numRookCaptures(self, board: List[List[str]]) -> int:
        for i in range(8):
            for j in range(8):
                if board[i][j] == 'R':
                    rook_x, rook_y = i, j
                    break

        directions = [(-1, 0), (1, 0), (0, -1), (0, 1)]
        captures = 0

        for dx, dy in directions:
            x, y = rook_x, rook_y
            while 0 <= x + dx < 8 and 0 <= y + dy < 8:
                x += dx
                y += dy
                if board[x][y] == 'B':
                    break
                if board[x][y] == 'p':
                    captures += 1
                    break

        return captures

